"""Generate the full experiment config tree, organised by model.

This is the single source of truth for *which* experiments exist. Adding a scenario,
encoder, or model is a one-line change in the spec below - re-run this script and the
whole `configs/` tree is rebuilt, so every encoding is wired the same way for every
scenario.

Layout produced:

    configs/{xgb,morais}/<scenario>_<encoder>.yaml

The downstream model is encoded by the *folder*; the experiment `name:` (used in
results) still carries the model suffix so rows stay unambiguous when aggregated.

Usage:
    python experiments/generate_configs.py
"""

from __future__ import annotations

import shutil
from pathlib import Path

import yaml

CONFIGS = Path(__file__).resolve().parents[1] / "configs"

# --------------------------------------------------------------------------- #
# Spec
# --------------------------------------------------------------------------- #
# Two downstream models, spanning two inductive biases: boosted trees (XGBoost) and the
# paper-faithful position-aided neural net of Morais et al. 2022 (`morais_nn`).
MODELS = {
    "xgb":    ("xgboost",   {}),
    "morais": ("morais_nn", {}),
}

# DeepSense beam prediction. Scenario 4 ships no `unit1_beam_index` column, but it does ship
# the `unit1_pwr_60ghz` power files, so the loader derives the target as argmax(power)+1 (see
# data_loading._derive_beam_index) - exactly the beam definition used by every other scenario.
# It is therefore included like all the rest. Every scenario carries (or derives) the beam
# target and resolves GPS, so all encoders apply uniformly (the loader uses each scenario's
# available base features - some lack the GPS-quality columns).
SCENARIO_IDS = [1, 2, 3, 4, 5, 6, 7, 8, 9]
SCENARIOS = {f"scenario{n}": f"datasets/deepsense/Scenario{n}" for n in SCENARIO_IDS}

# The encoders compared - the experiment's independent variable.
ENCODERS = ["baseline", "latlon", "timestamp", "lag_window", "rolling", "bs_geometry",
            "bs_bearing", "bs_rolling", "ple", "periodic"]

# Known-bad (model, encoder) combinations that are never generated. Add (mkey, encoder)
# pairs here to prune them.
EXCLUDED: set[tuple[str, str]] = set()

# Per-encoder default params (overrides applied per encoder name). None needed for the
# current encoder set.
ENCODER_PARAMS: dict[str, dict] = {}

_GROUPED = {"strategy": "grouped", "test_size": 0.30, "val_size_of_temp": 0.50}
_HEADER = "# Auto-generated by experiments/generate_configs.py - do not edit by hand.\n"


def _write(path: Path, cfg: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(_HEADER + yaml.safe_dump(cfg, sort_keys=False), encoding="utf-8")


def _generate() -> int:
    n = 0
    for mkey, (model_name, model_params) in MODELS.items():
        for scenario, path in SCENARIOS.items():
            for enc in ENCODERS:
                if (mkey, enc) in EXCLUDED:
                    continue
                cfg = {
                    "name": f"{scenario}_{enc}_{mkey}",
                    "dataset": {"kind": f"deepsense_{scenario}", "path": path,
                                "scenario_name": scenario},
                    "task": {"target": "unit1_beam_index", "group": "seq_index",
                             "type": "multiclass"},
                    "split": dict(_GROUPED),
                    "encoder": {"name": enc, "params": dict(ENCODER_PARAMS.get(enc, {}))},
                    "model": {"name": model_name, "params": dict(model_params)},
                    "evaluation": {"top_k": 3},
                    "results_dir": "results",
                }
                _write(CONFIGS / mkey / f"{scenario}_{enc}.yaml", cfg)
                n += 1
    return n


def main() -> None:
    # Clear the model folders (and any leftover from a previous nested/flat layout).
    for mkey in MODELS:
        shutil.rmtree(CONFIGS / mkey, ignore_errors=True)
    shutil.rmtree(CONFIGS / "time_series", ignore_errors=True)
    for p in CONFIGS.glob("*.yaml"):
        p.unlink()

    total = _generate()
    print(f"Generated {total} configs under {CONFIGS}/")
    for mkey in MODELS:
        print(f"  {mkey}: {len(list((CONFIGS / mkey).glob('*.yaml')))} configs")


if __name__ == "__main__":
    main()
